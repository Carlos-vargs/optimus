    private void jLabelBtnCreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelBtnCreateMouseClicked
        ArrayList<Integer> componentIds = new ArrayList<>();

        StoreComponentRequest ComponentRequest = new StoreComponentRequest();

        ComponentRequest.setParameter("panel", MiddlePanelComponent);
        ComponentRequest.setParameter("array", componentIds);

        ComponentController.store(ComponentRequest);

        StoreEquipmentRequest EquipmentRequest = new StoreEquipmentRequest();

        EquipmentRequest.setParameter("type", jComboBoxTypeEq.getSelectedItem().toString());
        EquipmentRequest.setParameter("brand", jComboBoxBrands.getSelectedItem().toString());
        EquipmentRequest.setParameter("state", jComboBoxStates.getSelectedItem().toString());
        EquipmentRequest.setParameter("acquisitionDate", jDateAcquisitionEq.getText());
        EquipmentRequest.setParameter("lastMaintenance", jDateLastMaintenance.getText());
        EquipmentRequest.setParameter("inventoryCode", jTextFieldInvCode.getText());
        EquipmentRequest.setParameter("componentIds", componentIds);

        if (EquipmentRequest.validate()) {
            EquipmentController.store(EquipmentRequest);
            showPanel(new EquipmentPanel());
        } else {
            setErrorMessage(jlabelErrorTypeEq, EquipmentRequest.getErrors().get("type"));
            setErrorMessage(jLabelErrorBrand, EquipmentRequest.getErrors().get("brand"));
            setErrorMessage(jLabelErrorState, EquipmentRequest.getErrors().get("state"));
            setErrorMessage(jLabelErrorAcquisitionDate, EquipmentRequest.getErrors().get("acquisitionDate"));
            setErrorMessage(jLabelErrorlastMaintenance, EquipmentRequest.getErrors().get("lastMaintenance"));
            setErrorMessage(JlabelErrorInventoryCode, EquipmentRequest.getErrors().get("inventoryCode"));
        }
    }//GEN-LAST:event_jLabelBtnCreateMouseClicked

