    private void AddComponentBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddComponentBtnMouseClicked
        MiddlePanelComponent.setLayout(new FlexWrapLayout(FlowLayout.LEFT, 40, 20));

        CreateComponentPanel component = (CreateComponentPanel) ComponentController.create();

        component.setComponentListener((CreateComponentPanel panel) -> {
            MiddlePanelComponent.remove(panel);
            MiddlePanelComponent.revalidate();
            MiddlePanelComponent.repaint();
        });

        MiddlePanelComponent.add(component);
        MiddlePanelComponent.revalidate();
        MiddlePanelComponent.repaint();

    }//GEN-LAST:event_AddComponentBtnMouseClicked

    private void ButtonHoverEffect(JLabel button, String defaultIconPath, String activeIconPath) {
        button.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseEntered(java.awt.event.MouseEvent evt) {
                button.setIcon(new ImageIcon(activeIconPath));
                button.setCursor(new Cursor(Cursor.HAND_CURSOR));
            }

            @Override
            public void mouseExited(java.awt.event.MouseEvent evt) {
                button.setIcon(new ImageIcon(defaultIconPath));
                button.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
            }
        });
    }

